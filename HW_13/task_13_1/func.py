def addition(first, second):
    result = first + second
    print(f'Result: {result}\n')


def subtraction(first, second):
    result = first - second
    print(f'Result: {result}\n')


def multiplication(first, second):
    result = first * second
    print(f'Result: {result}\n')


def division(first, second):
    result = first / second
    print(f'Result: {result}\n')
